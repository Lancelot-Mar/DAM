#include <stdio.h>
#include <stdlib.h>

int main(){
	char c1;
	
	c1='a';


	printf("%c+1=%i %c\n",c1,c1+1,c1+1);

	printf("%c+2=%i %c\n",c1,c1+2,c1+2);

	printf("%c+28=%i %c\n",c1,c1+28,c1+28);


	printf("%c-16=%i %c\n",c1,c1-16,c1-16);

	printf("%c/3=%i %c\n",c1,c1/3,c1/3);

	printf("%c*1=%i %c\n",c1,c1*1,c1*1);

	return 0;
}
